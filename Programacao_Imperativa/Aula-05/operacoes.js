// Comparador lógico &&

console.log(10 > 5 && 8 < 10);

// Comparador lógico && - Exemplo 2
console.log(10 > 5 && 8 > 10);

// Comparador lógico && - Exemplo 3
console.log(10 > 5 && 8 > 10 && 11 > 5);

// Comparador lógico OR (||)
console.log(10 > 5 || 8 > 10);

// Comparador de negação (!)
console.log(true);
console.log(!true);

console.log(!5 > 3);

// Operador de concatenação (+)
let nome = "João";
let sobrenome = "Silva";

console.log(nome + " " + sobrenome);